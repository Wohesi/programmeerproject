public class LargeTile_boardgame {

    public String name_large, desc, img;
    public int yearpublished, inplayers, maxplayers, playtime, age;

    public LargeTile_boardgame() {

    }

    // Setters & getters


    public String getName_large() {
        return name_large;
    }

    public void setName_large(String name_large) {
        this.name_large = name_large;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public int getYearpublished() {
        return yearpublished;
    }

    public void setYearpublished(int yearpublished) {
        this.yearpublished = yearpublished;
    }

    public int getInplayers() {
        return inplayers;
    }

    public void setInplayers(int inplayers) {
        this.inplayers = inplayers;
    }

    public int getMaxplayers() {
        return maxplayers;
    }

    public void setMaxplayers(int maxplayers) {
        this.maxplayers = maxplayers;
    }

    public int getPlaytime() {
        return playtime;
    }

    public void setPlaytime(int playtime) {
        this.playtime = playtime;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
